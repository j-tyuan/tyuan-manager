package org.tyuan.service.system.model.vo.sys;

import lombok.Getter;
import lombok.Setter;
import org.tyuan.common.ITree;

import java.io.Serializable;
import java.util.List;

@Setter
@Getter
public class MenuDataItemVo implements Serializable, ITree {

    private Long id;

    private Long parentId;

    private String name;

    private String icon;

    private String href;

    private String target;

    private Long sort;

    private boolean isLeaf;

    private List<ITree> children;

    /**
     * @name 在菜单中隐藏子节点
     */
    private boolean hideChildrenInMenu;

    /**
     * @name 在菜单中隐藏自己和子节点
     */
    private boolean hideInMenu;

    /**
     * String or bool
     *
     * @name 自定义菜单的国际化 key
     */
    private Object locale = false;

    /**
     * @name 用于标定选中的值，默认是 path
     */
    private String key;

    /**
     * @name disable 菜单选项
     */
    private boolean disabled;

    /**
     * @name 路径
     */
    private String path;

    /**
     * @name 自定义父节点
     * @description 当此节点被选中的时候也会选中 parentKeys 的节点
     */
    private String[] parentKeys;

    /**
     * @name 隐藏自己，并且将子节点提升到与自己平级
     */
    private boolean flatMenu;
}
